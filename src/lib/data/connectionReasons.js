// Helper function to get the right language
export function getConnectionReason(locale, type, elementId, frameworkSlug) {
  const lang = locale === 'sv' ? 'sv' : 'en';
  const reasons = connectionReasons[lang];
  
  if (!reasons) return '';
  
  if (type === 'practice' || type === 'level') {
    const practice = elementId;
    const reasonKey = getReasonKey(frameworkSlug);
    
    if (reasons.practices[practice] && reasons.practices[practice][reasonKey]) {
      return reasons.practices[practice][reasonKey];
    }
    
    return `${reasons.fallbacks.practiceConnection}`;
  }
  
  if (type === 'value') {
    const value = elementId;
    const reasonKey = getReasonKey(frameworkSlug);
    
    if (reasons.values[value] && reasons.values[value][reasonKey]) {
      return reasons.values[value][reasonKey];
    }
    
    return `${reasons.fallbacks.valueAlignment}`;
  }
  
  if (type === 'quiz') {
    return `${reasons.fallbacks.quizBased}`;
  }
  
  return '';
}

